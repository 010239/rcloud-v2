package com.chinasofti.rcloud.web.portal.controller.info;

public class Store {

	private String oimg;
	private String nimg;
	private String href;
	private String name;
	public String getOimg() {
		return oimg;
	}
	public void setOimg(String oimg) {
		this.oimg = oimg;
	}
	public String getNimg() {
		return nimg;
	}
	public void setNimg(String nimg) {
		this.nimg = nimg;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
